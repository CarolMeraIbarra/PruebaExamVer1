# Función para imprimir resultados largos
def imprimir_resultados_largo(datos_ordenados, valor_minimo, valor_maximo, rango, numero_intervalos, ancho_clase, frecuencias, frecuencias_relativas, frecuencias_acumuladas):
    print(f'Datos ordenados: {datos_ordenados}')
    print(f'Valor mínimo: {valor_minimo}')
    print(f'Valor máximo: {valor_maximo}')
    print(f'Rango: {rango}')
    print(f'Número de intervalos: {numero_intervalos}')
    print(f'Ancho de clase: {ancho_clase}')
    print(f'Frecuencia absoluta: {frecuencias}')
    print(f'Frecuencia relativa: {frecuencias_relativas}')
    print(f'Frecuencia acumulada: {frecuencias_acumuladas}')
    print()